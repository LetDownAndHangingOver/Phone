package com.benali.entitites;

public class ComparateurForm {
	private Long idsmart1;
	private Long idsmart2;
	
	
	public ComparateurForm() {
		super();
	}
	public ComparateurForm(Long idsmart1, Long idsmart2) {
		super();
		this.idsmart1 = idsmart1;
		this.idsmart2 = idsmart2;
	}
	public Long getIdsmart1() {
		return idsmart1;
	}
	public void setIdsmart1(Long idsmart1) {
		this.idsmart1 = idsmart1;
	}
	public Long getIdsmart2() {
		return idsmart2;
	}
	public void setIdsmart2(Long idsmart2) {
		this.idsmart2 = idsmart2;
	}
	

}
